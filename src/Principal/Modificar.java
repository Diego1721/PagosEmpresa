    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        PS = null;
        CN = new Conectar();
        Connection con;
        String BusCed2;
        BusCed2 = ModifE.getText();
        try {
            con = CN.getConnection();
            PS = con.prepareStatement("SELECT * FROM registro WHERE Cedula = ? ");
            PS.setString(1, BusCed2);
            rs = PS.executeQuery();
            if (rs.next()) {
                ModifNom.setText(rs.getString("Nombre"));
                ModifTel.setText(rs.getString("Telefono"));
                ModifHor.setText(String.valueOf(rs.getInt("Horas")));
                ModifValH.setText(String.valueOf(rs.getInt("ValorH")));
                ModifSuel.setText(String.valueOf(rs.getString("Salario")));
            } else {
                ModifNom.setText("");
                ModifTel.setText("");
                ModifHor.setText("");
                ModifValH.setText("");
                ModifSuel.setText("");
                if (BusCed2.isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Porfavor Ingrese una cedula");
                } else {
                    JOptionPane.showMessageDialog(null, "No se encontro la cedula");
                }
            }
        } catch (Exception e) {
            System.err.println(e);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String Hor, Mod, Nom, Tel, ValH, BusCed2;
        int Suel;
        Salario calc = new Salario();
        ValH = ModifValH.getText();
        Hor = ModifHor.getText();
        Nom = ModifNom.getText();
        Tel = ModifTel.getText();
        Suel = calc.getResueldo();
        BusCed2 = ModifE.getText();
        PS = null;
        CN = new Conectar();
        if (BusCed2.isEmpty()||Nom.isEmpty()|Tel.isEmpty()||ValH.isEmpty()||Hor.isEmpty()) {
            JOptionPane.showMessageDialog(null, "No hay cedula a la cual se le puedan modificar datos o los datos estan incompletos ");
        } else {
            try {
                Salario aux = new Salario();
                aux.setNom(Nom);
                aux.setValorH(Integer.valueOf(ValH));
                aux.setHoras(Integer.valueOf(Hor));
                aux.setTel(Tel);
                aux.setResueldo(Suel);
                PS = CN.getConnection().prepareStatement("UPDATE registro SET Nombre = ?,Telefono = ?,Horas = ?, ValorH = ?, Salario = ? WHERE Cedula = ?");
                PS.setString(1, aux.getNom());
                PS.setString(2, aux.getTel());
                PS.setInt(3, aux.getHoras());
                PS.setInt(4, aux.getValorH());
                PS.setInt(5, aux.getResueldo());
                PS.setString(6, BusCed2);
                int res = PS.executeUpdate();
                if (res > 0) {
                    JOptionPane.showMessageDialog(null, "Registro Modificado ");
                } else {
                    JOptionPane.showMessageDialog(null, "El registro no pudo ser modificado");
                }
            } catch (HeadlessException | SQLException e) {
                System.err.println("Error al Cargar " + e.getMessage());
            }
            ModifE.setText("");
            ModifNom.setText("");
            ModifTel.setText("");
            ModifHor.setText("");
            ModifValH.setText("");
            ModifSuel.setText("");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

